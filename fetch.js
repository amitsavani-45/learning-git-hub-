/*git fetch

Kya karta hai:
👉 Remote repository (GitHub) se latest changes download karta hai
👉 Lekin tumhari current files ko change nahi karta

Simple language:

“Sirf check karo aur download karo, merge mat karo”

Example:

git fetch

Kab use kare:

Jab dekhna ho ki remote me kya naya aaya

Safe update chahiye without merge

Difference (important):

git fetch → download only

git pull → download + merge*/